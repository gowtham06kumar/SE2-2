package com.univ.beans;

public class Faculty {

	String firstName;
	String lastName;
	String gradSchool;
	String degree;
	String title;
	String daysToTeach;
	String maxLoadFall;
	String maxLoadSpring;
	String maxLoadSummer;

	public String getFirstName() {
		return firstName;
	}

	public void setFirstName(String firstName) {
		this.firstName = firstName;
	}

	public String getLastName() {
		return lastName;
	}

	public void setLastName(String lastName) {
		this.lastName = lastName;
	}

	public String getGradSchool() {
		return gradSchool;
	}

	public void setGradSchool(String gradSchool) {
		this.gradSchool = gradSchool;
	}

	public String getDegree() {
		return degree;
	}

	public void setDegree(String degree) {
		this.degree = degree;
	}

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getDaysToTeach() {
		return daysToTeach;
	}

	public void setDaysToTeach(String daysToTeach) {
		this.daysToTeach = daysToTeach;
	}

	public String getMaxLoadFall() {
		return maxLoadFall;
	}

	public void setMaxLoadFall(String maxLoadFall) {
		this.maxLoadFall = maxLoadFall;
	}

	public String getMaxLoadSpring() {
		return maxLoadSpring;
	}

	public void setMaxLoadSpring(String maxLoadSpring) {
		this.maxLoadSpring = maxLoadSpring;
	}

	public String getMaxLoadSummer() {
		return maxLoadSummer;
	}

	public void setMaxLoadSummer(String maxLoadSummer) {
		this.maxLoadSummer = maxLoadSummer;
	}
}
